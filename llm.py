from __future__ import annotations

import json
import logging

from pathlib import Path
from datetime import datetime

logger = logging.getLogger("app.llm")


def save_llm_interaction(
    system_prompt: str,
    user_prompt: str,
    response: str,
) -> None:
    """
    Сохраняет полный запрос и ответ модели.
    """

    PROJECT_ROOT = Path(__file__).resolve().parent
    log_dir = PROJECT_ROOT / "outputs" / "judge_logs"
    log_dir.mkdir(parents=True, exist_ok=True)

    timestamp = datetime.now().strftime("%Y%m%d_%H%M%S_%f")

    file_path = log_dir / f"judge_{timestamp}.txt"

    with open(file_path, "w", encoding="utf-8") as f:

        f.write("=" * 80)
        f.write("\nSYSTEM PROMPT\n")
        f.write("=" * 80)
        f.write("\n\n")
        f.write(system_prompt)

        f.write("\n\n")
        f.write("=" * 80)
        f.write("\nUSER PROMPT\n")
        f.write("=" * 80)
        f.write("\n\n")
        f.write(user_prompt)

        f.write("\n\n")
        f.write("=" * 80)
        f.write("\nMODEL RESPONSE\n")
        f.write("=" * 80)
        f.write("\n\n")
        f.write(response)

    logger.info("LLM interaction saved to %s", file_path)


